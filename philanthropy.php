            <p>In 1936, Eta chapter member, Ruth Billow, made a plea to Delta Gamma's convention body to adopt Service for Sight and Aid to the Blind as the Fraternity's National Philanthropy. Since then, Delta Gammas all over the world have been working to aid the visually impaired.</p>
            <p>The women at Chi have been a model of the fraternity’s goals to aid the blind through both their physical and fiscal efforts. Specifically the women of our chapter contribute by making Braille cards around the holidays to send to local hospitals and care centers with visually impaired patients. Chi's Braille cards are sent to Military veterans who have suffered from blindness in the New York State area.</p>
            <p>Delta Gammas volunteer in a variety of ways in the community on their own time, however collectively, we pledge our time each semester to helping the visually impaired in the community to support Service for Sight and actively help the cause. Chi chapter volunteers friday afternoons at Beechtree Care Center in downtown Ithaca to interact with and cheer up with elderly and sight impaired patients. Our girls always enjoy our time there and the patients of Beechtree always enjoy our visits as well! Chi also hosts events to raise money for the organization and participates in Guiding Eyes for the Blind, a great on-campus organization that shares our purpose of helping the visually impaired!</p>
