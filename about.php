                    <p>As the president of the Chi chapter of Delta Gamma at Cornell University, it is my pleasure to welcome you to our official website! I invite you to browse our site to learn more about our chapter and what it means to be a Delta Gamma.</p>
                    <p>Our chapter is made up of over 125 incredibly talented, diverse, and intelligent women, and every unique woman serves as an inspiration to me on a daily basis. Cornell Delta Gammas are involved all over campus. From writing for the Cornell Daily Sun, to competing on varsity sports teams and holding leadership positions throughout the Cornell community, our sisters are extremely active on Cornell's campus.  It is no surprise that Cornell Delta Gammas obtain some of the most competitive jobs and internships every year.</p>
                    <p>Despite how busy we all are, one common thread that links us all is our passion to “do good.” This is our motto, and we accomplish this every year through our service initiatives. Our members give their time and talent to raise money for our philanthropy, Service for Sight. Through these efforts, as well as throughout our daily lives, we fulfill our primary purpose, set by our founders in 1873: “We encourage an atmosphere which will foster high ideals of friendship among college women, promote their educational and cultural interests, create in them a true sense of social responsibility and develop in them the best qualities of character.”</p>
                    <p>When I joined Delta Gamma three years ago, I had no idea how transformative it would be for me, and I now reflect upon the decision I made to join Delta Gamma as undoubtedly one of the best choices I have made at Cornell. I hope this website gives you a glimpse into our sisterhood and why Delta Gamma is not just for four years, but for life.</p>
