            <h4>recruitment schedule</h4>
            <h5>Please visit the Cornell Panhellenic website <a href="http://www.cornellphc.org/recruitment/">here</a> for more information!</h5>
            
            <h6>Round 1 - Thursday, January 19 & Friday, January 20</h6>
            <p>During these first two days, you will be welcomed into each of the thirteen Panhellenic chapter houses to meet and talk to women from each organization. This is the “Speed Dating” round, due to the quick and relaxed nature of the conversations you will have. You will go to six houses on the first day and seven houses on the next. At the end of Day 2, you will rank in order of preference the eight houses that you would like to return to the following day.</p>
            
            <h6>Round 2 - Saturday, January 21</h6>
            <p>This is “House Tours” day, where you can return to a maximum of eight houses.  Each chapter will give you a tour of their home to show you what it is like to live-in as a member of that house!  It is important to take note of where you can see yourself living and of what makes each chapter unique. At the end of Day 3, you will rank the houses you visited in order of the (maximum) five houses to which you want to return. </p>
            
            <h6>Round 3 - Sunday, January 22</h6>
            <p>On this day, you will be invited back to a maximum of five houses. During the “Philanthropy Round” each house will introduce you to their national chapter’s philanthropic foundation and explain to you some of the philanthropy events they partake in during the year. Each house will also perform a Skit to give you a glimpse of the personalities and values of the members of that chapter. This round shows you the sense of humor of each chapter and what it means to be in each respective house, as well as the passion and devotion each house has to their philanthropy. At the end of Day 4, you will rank the houses you attended in order of the (maximum) three chapters that you want to return to for Preference Round.</p>
            
            <h6>Round 4 - Monday, January 23</h6>
            <p>“Preference Round” is the most intimate and special round of Recruitment.  On this day you will return to a maximum of three houses and at the end of the day you will decide which house is the right fit for you. When visiting each house, think about the conversations you had and the connections you made with its members, how you feel when you walk in the door, and more importantly how you feel when you walk out. Also, take the opportunity to ask the women you speak with any questions you may still have that will help you make your final decision. After rounds have ended, you will head to Willard Straight Hall to reflect on your experiences at each house during the entire week and to submit your ranked preferences for the chapters you visited that day. Through the mutual selection process, we hope that you find your new home in the Panhellenic community!</p>
            
            <h6>Bid Night! Tuesday, January 24</h6>
            <p>On Tuesday afternoon, you will receive your bid cards and head to the house you have been invited to join! Your new sisters will welcome you and the celebrations will commence!</p>
